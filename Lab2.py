# === Створюємо список із 10 цілих чисел і 10 рядків ===
nums = [10, 3, 8, 15, 2, 7, 1, 20, 12, 5]      # список цілих (int)
words = ["яблуко", "банан", "груша", "абрикос", "вишня", "диня", "ківі", "персик", "слива", "апельсин"]  # список рядків (str)

# Об’єднуємо два списки в один
main_list = nums + words

# === Сортуємо: спочатку числа за зростанням, потім слова за алфавітом ===
sorted_list = sorted(nums) + sorted(words)

# === Знаходимо всі числа, кратні 2 ===
even_numbers = [n for n in sorted_list if isinstance(n, int) and n % 2 == 0]

# === Робимо окремий список, де всі слова з головного — великими літерами ===
upper_words = [w.upper() for w in sorted_list if isinstance(w, str)]

# === Виводимо результати ===
print("=== Головний список ===")
print(main_list)
print("\n=== Відсортований список (числа ↑, слова A→Я) ===")
print(sorted_list)
print("\n=== Числа, кратні 2 ===")
print(even_numbers)
print("\n=== Слова великими літерами ===")
print(upper_words)
